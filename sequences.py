# sequences.py

# Tuples: Immutable sequences
my_tuple = (1, 2, 3)
print(f"Tuple: {my_tuple}")

# Lists: Mutable sequences
my_list = [1, 2, 3, 4]
my_list.append(5)
print(f"List after appending: {my_list}")

# Strings: Immutable sequences of characters
my_string = "Hello"
print(f"String: {my_string}")
print(f"First character: {my_string[0]}")

# Slicing a sequence
slice_example = my_list[1:4]
print(f"Sliced list: {slice_example}")

